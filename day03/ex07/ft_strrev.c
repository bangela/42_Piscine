char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	temp;

	i = 0;
	n = 0;
	while (str[n] != '\0')
		n++;
	while (i < n / 2)
	{
		temp = str[i];
		str[i] = str[n - i - 1];
		str[n - i - 1] = temp;
		i++;
	}
	return (str);
}
